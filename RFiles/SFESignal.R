# Description:  SFESignal simulates signal St = 2cos {2π(t/50) + 0.6π},     
#               with εt1 ∼N(0,1)and εt2 ∼N(0,25) and plots the results
# ------------------------------------------------------------------------------
# Usage:        -
# ------------------------------------------------------------------------------
# Inputs:       none
# ------------------------------------------------------------------------------
# Output:       Line plots of St, St+εt1, St+εt2
# ------------------------------------------------------------------------------
# Example:      -
# ------------------------------------------------------------------------------
# Author:       Dedy Dwi Prastyo, Elisabeth Bommes, Caroline Kleist
# ------------------------------------------------------------------------------
#

t   = 1:500
S   = 2*cos(2*pi*t/50 + .6*pi)
εt1 = rnorm(500,0,1)
εt2 = 5*εt1

par(mfrow=c(3,1),mex=1,lheight=1.5)
plot.ts(S, xlab="")
plot.ts(S + εt1, xlab="")
plot.ts(S + εt2)
